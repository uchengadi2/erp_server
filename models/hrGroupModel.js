const mongoose = require("mongoose");

const hrGroupSchema = new mongoose.Schema(
  {
    name: {
      type: String,
      required: [true, "This field cannot be empty"],
    },
    description: {
      type: String,
      trim: true,
    },
  },
  {
    toJSON: { virtuals: true },
    toObject: { virtuals: true },
  }
);

const HrGroup = mongoose.model("HrGroup", hrGroupSchema);

module.exports = HrGroup;
