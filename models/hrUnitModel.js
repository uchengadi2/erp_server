const mongoose = require("mongoose");

const hrUnitSchema = new mongoose.Schema(
  {
    name: {
      type: String,
      required: [true, "This field cannot be empty"],
    },
    description: {
      type: String,
      trim: true,
    },
  },
  {
    toJSON: { virtuals: true },
    toObject: { virtuals: true },
  }
);

const HrUnit = mongoose.model("HrUnit", hrUnitSchema);

module.exports = HrUnit;
